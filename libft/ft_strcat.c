char		*ft_strcat(char *s1, char *s2)
{
	int		i;
	int		j;

	i = -1;
	while (s1[++i])
		;
	j = -1;
	while (s2[++j])
		s1[i + j] = s2[j];
	s1[i + j] = '\0';
	return (s1);
}
