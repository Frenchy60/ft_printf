#include <string.h>

char			*ft_strstr(char *s1, char *s2)
{
	size_t		i;

	if (!*s2)
		return (s1);
	while (*s1)
	{
		i = 0;
		while (s1[i] == s2[i] && s2[i] != '\0')
			i++;
		if (s2[i] == '\0')
			return (s1);
		s1++;
	}
	return (0);
}
