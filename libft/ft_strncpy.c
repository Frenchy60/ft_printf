#include <string.h>

char		*ft_strncpy(char *dst, char *src, size_t len)
{
	size_t	i;

	i = -1;
	while (++i < len)
		if (*(src + i))
			*(dst + i) = *(src + i);
		else
			while (i < len)
				*(dst + i++) = '\0';
	return (dst);
}
